# Arithmetic operator

x = 10
y = 5

print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y)
print(x ** y)

#Comparison operator

print(x == y)
print(x >= y)
print(x <= y)
print(x != y)
print(x > y)
print(x < y)

#Logical operator

print(x>y and x<y)
print(x>y or x<y)

#Assignment operator 

a = 2
b = 3

a = a + 6
a += 6
print(a)
b = b + 2
b += 2
print(b)